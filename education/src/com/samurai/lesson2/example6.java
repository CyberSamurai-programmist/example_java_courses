package com.samurai.lesson2;

/**
 * Даны 2 числа. Вывести большее из них
 */
public class example6 {
    public static void main(String[] args) {
        int a = 32;
        int b = 26;

        if (a > b) {
            System.out.println("Большее число " + a);
        } else {
            System.out.println("Большее число " + b);
        }
    }
}
